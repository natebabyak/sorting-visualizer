/**
 * The big O notation of a complexity.
 */
export enum Complexity {
  Constant = '\\( O(1) \\)',
  Logarithmic = '\\( O(\\log n) \\)',
  Linear = '\\( O(n) \\)',
  Linearithmic = '\\( O( n\\log n) \\)',
  Quadratic = '\\( O(n^2) \\)',
  Exponential = '\\( O(2^n) \\)',
  Factorial = '\\( O(n!) \\)'
}
